document.getElementById('card-heart1').addEventListener('click', function(e){ 
    e.preventDefault()
    const navbarHeart = document.getElementById('navbar-heart')
    let counting = parseInt(navbarHeart.innerText)
    counting ++ 
    navbarHeart.innerText = counting
    
})
